# Demonstrating the use of logical operators in Python

# Logical AND
a = True
b = False
print("a and b:", a and b)  # Output: False

# Logical OR
print("a or b:", a or b)  # Output: True

# Logical NOT
print("not a:", not a)  # Output: False
print("not b:", not b)  # Output: True

# Combining logical operators
x = 10
y = 20
z = 30
print("x < y and y < z:", x < y and y < z)  # Output: True
print("x > y or y < z:", x > y or y < z)   # Output: True